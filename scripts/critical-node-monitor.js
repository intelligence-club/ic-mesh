#!/usr/bin/env node
/**
 * Critical Node Monitor
 * Monitors high-value nodes and triggers alerts when they go offline
 * Helps prevent capacity crises by early detection of critical node loss
 */

const sqlite3 = require('sqlite3').verbose();
const fs = require('fs').promises;
const path = require('path');

const DB_PATH = path.join(__dirname, '../data/mesh.db');

// Configuration
const CRITICAL_NODE_CRITERIA = {
    minJobsCompleted: 50,        // Nodes with 50+ completed jobs are critical
    minUptimeMinutes: 1000,      // Nodes with 16+ hours uptime are critical
    maxOfflineMinutes: 30,       // Alert if critical node offline > 30 minutes
    alertCooldown: 60            // Only alert once per hour per node
};

class CriticalNodeMonitor {
    constructor() {
        this.lastAlerts = new Map(); // Track when alerts were last sent
        this.alertLogFile = path.join(__dirname, '../data/critical-node-alerts.log');
    }

    async checkCriticalNodes() {
        return new Promise((resolve, reject) => {
            const db = new sqlite3.Database(DB_PATH);
            const now = Date.now();
            
            db.all(`
                SELECT 
                    nodeId,
                    name,
                    capabilities,
                    jobsCompleted,
                    computeMinutes,
                    lastSeen,
                    registeredAt,
                    owner,
                    (? - lastSeen) / (1000 * 60) as minutesOffline,
                    (? - registeredAt) / (1000 * 60) as totalUptimeMinutes
                FROM nodes
                WHERE jobsCompleted >= ? OR computeMinutes >= ?
                ORDER BY jobsCompleted DESC, computeMinutes DESC
            `, [now, now, CRITICAL_NODE_CRITERIA.minJobsCompleted, CRITICAL_NODE_CRITERIA.minUptimeMinutes], (err, rows) => {
                db.close();
                if (err) return reject(err);
                resolve(rows);
            });
        });
    }

    async logAlert(nodeId, nodeName, alertType, details) {
        const timestamp = new Date().toISOString();
        const logEntry = `[${timestamp}] ${alertType}: ${nodeName} (${nodeId.slice(0,8)}) - ${details}\\n`;
        
        await fs.appendFile(this.alertLogFile, logEntry, 'utf8');
        console.log(`🚨 ALERT: ${alertType} - ${nodeName} - ${details}`);
    }

    async createUrgentAlert(node) {
        const timestamp = new Date().toISOString();
        const alertId = `CRITICAL-NODE-ALERT-${timestamp.replace(/[:.]/g, '-')}`;
        const alertFile = path.join(__dirname, `../../${alertId}.md`);
        
        const capabilities = JSON.parse(node.capabilities || '[]');
        const revenueEstimate = node.jobsCompleted * 0.25; // Rough estimate: $0.25 per job
        
        const alertContent = `# 🚨 CRITICAL NODE OFFLINE ALERT

**Alert ID:** ${alertId}
**Generated:** ${timestamp}
**Priority:** URGENT
**Node:** ${node.name} (${node.nodeId.slice(0,8)})

## 💥 IMPACT ASSESSMENT

**Performance History:**
- **Jobs completed:** ${node.jobsCompleted} (HIGH PERFORMER)
- **Total runtime:** ${Math.round(node.totalUptimeMinutes)} minutes (${Math.round(node.totalUptimeMinutes/60)} hours)
- **Est. revenue contribution:** ~$${revenueEstimate.toFixed(2)}
- **Capabilities:** ${capabilities.join(', ')}
- **Owner:** ${node.owner || 'unknown'}

**Offline Duration:** ${Math.round(node.minutesOffline)} minutes

## 📊 CAPACITY IMPACT

This node provides critical capabilities: **${capabilities.join(', ')}**

**Immediate Actions Required:**
1. Check if node owner can restart/reconnect
2. Monitor for auto-recovery (may reconnect automatically)
3. Activate backup capacity if available
4. Contact node operator: ${node.owner || 'unknown'}

## 🔄 RECOVERY OPTIONS

### Auto-Recovery Monitoring
- Historical pattern: High-value nodes often auto-reconnect within 30-60 minutes
- **Monitor for:** Automatic reconnection (check every 5-10 minutes)

### Manual Recovery
- **Contact owner:** ${node.owner || 'Contact needed'}
- **Node restart:** May require operator intervention
- **Backup activation:** Consider activating alternative nodes

## ⏰ ESCALATION TIMELINE

- **T+30min:** If no auto-recovery, escalate to direct contact
- **T+60min:** If no response, activate emergency backup capacity
- **T+120min:** Consider emergency capacity recruitment

---
*Alert generated by Critical Node Monitor*
*Next check: Monitor for recovery every 10 minutes*
`;

        await fs.writeFile(alertFile, alertContent, 'utf8');
        console.log(`📄 Urgent alert created: ${alertId}.md`);
        
        return alertFile;
    }

    shouldAlert(nodeId, alertType) {
        const lastAlertKey = `${nodeId}-${alertType}`;
        const lastAlert = this.lastAlerts.get(lastAlertKey);
        const now = Date.now();
        
        if (!lastAlert || (now - lastAlert) > (CRITICAL_NODE_CRITERIA.alertCooldown * 60 * 1000)) {
            this.lastAlerts.set(lastAlertKey, now);
            return true;
        }
        
        return false;
    }

    async monitor() {
        try {
            const criticalNodes = await this.checkCriticalNodes();
            
            console.log(`🔍 Monitoring ${criticalNodes.length} critical nodes...`);
            
            for (const node of criticalNodes) {
                const isOffline = node.minutesOffline > 5; // Consider offline if no heartbeat for 5+ minutes
                const isLongOffline = node.minutesOffline > CRITICAL_NODE_CRITERIA.maxOfflineMinutes;
                
                if (isOffline) {
                    console.log(`⚠️  ${node.name} offline: ${Math.round(node.minutesOffline)} minutes (${node.jobsCompleted} jobs completed)`);
                    
                    if (isLongOffline && this.shouldAlert(node.nodeId, 'OFFLINE')) {
                        await this.logAlert(node.nodeId, node.name, 'OFFLINE', 
                            `High-performer offline ${Math.round(node.minutesOffline)}min (${node.jobsCompleted} jobs completed)`);
                        
                        // Create urgent alert file for manual review
                        await this.createUrgentAlert(node);
                    }
                } else {
                    console.log(`✅ ${node.name} online: ${node.jobsCompleted} jobs completed`);
                }
            }
            
            return criticalNodes.filter(node => node.minutesOffline <= 5).length; // Return online count
            
        } catch (error) {
            console.error(`❌ Monitor error: ${error.message}`);
            return -1;
        }
    }

    async getStatus() {
        const criticalNodes = await this.checkCriticalNodes();
        const onlineNodes = criticalNodes.filter(node => node.minutesOffline <= 5);
        const offlineNodes = criticalNodes.filter(node => node.minutesOffline > 5);
        
        return {
            total: criticalNodes.length,
            online: onlineNodes.length,
            offline: offlineNodes.length,
            longOffline: offlineNodes.filter(node => node.minutesOffline > CRITICAL_NODE_CRITERIA.maxOfflineMinutes).length,
            nodes: {
                online: onlineNodes,
                offline: offlineNodes
            }
        };
    }
}

// CLI Interface
async function main() {
    const monitor = new CriticalNodeMonitor();
    
    if (process.argv[2] === '--status') {
        const status = await monitor.getStatus();
        console.log(`📊 Critical Node Status:`);
        console.log(`   Total: ${status.total} critical nodes`);
        console.log(`   Online: ${status.online} | Offline: ${status.offline}`);
        console.log(`   Long offline (${CRITICAL_NODE_CRITERIA.maxOfflineMinutes}+ min): ${status.longOffline}`);
        return;
    }
    
    if (process.argv[2] === '--watch') {
        console.log('🚨 Critical Node Monitor - Watch Mode');
        console.log(`   Alert threshold: ${CRITICAL_NODE_CRITERIA.maxOfflineMinutes} minutes`);
        console.log(`   Checking every 60 seconds...\\n`);
        
        const checkLoop = async () => {
            await monitor.monitor();
            console.log(`   Next check in 60s...\\n`);
        };
        
        // Initial check
        await checkLoop();
        
        // Set up interval
        const interval = setInterval(checkLoop, 60000);
        
        // Graceful shutdown
        process.on('SIGINT', () => {
            console.log('\\n👋 Monitor stopping...');
            clearInterval(interval);
            process.exit(0);
        });
        
        return;
    }
    
    // Default: single check
    await monitor.monitor();
}

if (require.main === module) {
    main().catch(console.error);
}

module.exports = CriticalNodeMonitor;